"""Escreva um algoritmo para ler as 
dimensões de um retângulo (base e altura), 
calcular e escrever a área do retângulo e a  do triangulo. 
"""

base = float(input('digite o valor base'))

altura = float(input('digite a  altura'))

retangulo = base*altura

triangulo = (base*altura)/2

print(f" A area do retangulo {retangulo} e triangulo {triangulo}")
